#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*tmp;

	tmp = (char *)s + ft_strlen(s);
	while (*tmp != c)
	{
		if (tmp == s)
		{
			return (NULL);
		}
		tmp--;
	}
	return (tmp);
}
