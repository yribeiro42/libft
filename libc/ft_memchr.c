#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*tmp;

	tmp = (unsigned char *)s;
	while (n--)
	{
		if (*tmp == c)
			return (tmp);
		if (n)
			tmp++;
	}
	return (NULL);
}
